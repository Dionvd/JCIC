package entity;

public class Node 
{
	int power = 0;
	int ownerId = 0;
       
    public Node() {
    }

	public int getPower() {
		return power;
	}

	public void setPower(int power) {
		this.power = power;
	}

	public int getOwnerId() {
		return ownerId;
	}

	public void setOwnerId(int ownerId) {
		this.ownerId = ownerId;
	}

 
    

    
}
